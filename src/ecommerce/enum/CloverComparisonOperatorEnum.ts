export enum CloverComparisonOperatorEnum {
    /**
     * Retrieve results where the created field is greater than the current value.
     */
    GT = 'gt',
    /**
     * Retrieve results where the created field is greater than or equal to the current value.
     */
    GTE = 'gte',
    /**
     * Retrieve results where the created field is less than the current value.
     */
    LT = 'lt',
    /**
     * Retrieve results where the created field is less than or equal to the current value.
     */
    LTE = 'lte',
}
